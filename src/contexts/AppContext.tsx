interface AppContextType {
  theme: typeof themes.orange;
  themeName: keyof typeof themes;
  setTheme: (themeName: keyof typeof themes) => void;
  language: "vi" | "en";
  setLanguage: (lang: "vi" | "en") => void;
  t: (key: string) => string;
}

const AppContext = createContext<AppContextType | undefined>(undefined);

export const AppProvider = ({ children }: { children: React.ReactNode }) => {
  const [themeName, setThemeName] = useState<keyof typeof themes>("orange");
  const [language, setLanguage] = useState<"vi" | "en">("vi");

  useEffect(() => {
    loadSettings();
  }, []);

  const loadSettings = async () => {
    try {
      const savedTheme = await AsyncStorage.getItem("appTheme");
      const savedLanguage = await AsyncStorage.getItem("appLanguage");
      if (savedTheme && themes[savedTheme as keyof typeof themes]) {
        setThemeName(savedTheme as keyof typeof themes);
      }
      if (savedLanguage && (savedLanguage === "vi" || savedLanguage === "en")) {
        setLanguage(savedLanguage);
      }
    } catch (error) {
      console.error("Error loading settings:", error);
    }
  };

  const changeTheme = async (newTheme: keyof typeof themes) => {
    try {
      await AsyncStorage.setItem("appTheme", newTheme);
      setThemeName(newTheme);
    } catch (error) {
      console.error("Error saving theme:", error);
    }
  };

  const changeLanguage = async (lang: "vi" | "en") => {
    try {
      await AsyncStorage.setItem("appLanguage", lang);
      setLanguage(lang);
    } catch (error) {
      console.error("Error saving language:", error);
    }
  };

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <AppContext.Provider
      value={{
        theme: themes[themeName],
        themeName,
        setTheme: changeTheme,
        language,
        setLanguage: changeLanguage,
        t,
      }}
    >
      {children}
    </AppContext.Provider>
  );
};

export const useApp = () => {
  const context = useContext(AppContext);
  if (!context) {
    throw new Error("useApp must be used within AppProvider");
  }
  return context;
};
